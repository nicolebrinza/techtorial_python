a = 25
b = 5
# following line will give me the class of a and b -> int
print(type(a))
print(type(b))

multiplication = a * b
print(type(multiplication))
# int

addition = a + b
print(type(addition))
# int

subtraction = a - b
print(type(subtraction))
# int

division = a / b
print(type(division))
# float
