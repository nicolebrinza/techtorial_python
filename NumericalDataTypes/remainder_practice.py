var1 = 5
var2 = 21
result = var1 % var2
print(result)
# it will display 5 (cause left number is smaller than right number, the remainder will be the value of left number)
result2 = var2 % var1
print(result2)
# it will display 1

number1 = 32.75
number2 = .5
print(number1 % number2)
# in python .5 or .50 or .500 or 0.5 is the same thing
